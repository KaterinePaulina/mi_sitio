<h1>El origen de la musica clasica</h1>
<div class="cont_img">
  <img src="img/clasica.jpg" alt="">
</div>
<div class="cont_inf">
<p> La música clásica, también conocida como música culta, erudita o académica, tiene sus raíces en la tradición musical de Europa occidental. Su desarrollo se remonta a la música religiosa y secular de la Edad Media y el Renacimiento, aproximadamente entre los siglos IX y XV. Durante este período, la Iglesia desempeñó un papel crucial en la promoción y preservación de la música, lo que llevó al surgimiento de formas musicales como el canto gregoriano.

<br><br>
Con el paso del tiempo, la música clásica evolucionó a través de distintos periodos como el Barroco (siglos XVII y XVIII), el Clasicismo (siglo XVIII), el Romanticismo (siglo XIX) y el siglo XX, cada uno con sus propias características estilísticas y expresivas. Compositores como Johann Sebastian Bach, Wolfgang Amadeus Mozart, Ludwig van Beethoven y muchos otros han dejado un legado duradero en este género musical.

<br><br>
La música clásica ha sido transmitida a través de partituras y ha sido interpretada por orquestas, coros y solistas en salas de conciertos de todo el mundo. Su influencia en la música contemporánea es innegable, y su legado sigue siendo una fuente de inspiración para músicos y amantes de la música en la actualidad
</p>
</div>
</section>

