    <h2>¿Por que se popularizo?</h2>
    <p> La popularización de la música clásica ha ocurrido en diversas sociedades a lo largo del tiempo. En la actualidad, es apreciada en todo el mundo, pero su popularidad ha variado a lo largo de la historia y en diferentes regiones.
<br><br>
Por ejemplo, durante el periodo barroco, la música clásica era especialmente popular en las cortes reales y entre la nobleza europea, mientras que en el siglo XIX se expandió a un público más amplio gracias a conciertos públicos y la creación de orquestas sinfónicas. En el siglo XX, con la llegada de la grabación de música, la música clásica se difundió aún más, llegando a ser apreciada por personas de distintas clases sociales en todo el mundo.
<br><br>
Además de su presencia en la cultura popular y su adaptación a otros géneros musicales, la música clásica se ha popularizado gracias a la disponibilidad de grabaciones y transmisiones en vivo. La tecnología ha permitido que las obras maestras de la música clásica estén al alcance de un público más amplio a través de grabaciones en vinilo, CD, streaming y transmisiones en línea. 
</p>
  </div>
  <div class="column">
    <h2>Su principal audiencia </h2>
    <p>Inicialmente, la música clásica solía ser escuchada por la nobleza y la clase alta en Europa, ya que los compositores trabajaban para cortes reales y aristocráticas. Con el tiempo, especialmente a partir del siglo XIX, la música clásica se volvió más accesible al público en general, atrayendo a personas de diversos estratos sociales a conciertos y óperas. Hoy en día, la audiencia de música clásica es muy diversa e incluye a personas de diferentes ámbitos sociales, desde estudiantes hasta profesionales y amantes de la música en general. La música clásica ha logrado trascender barreras sociales y es apreciada por una amplia gama de personas en todo el mundo.
