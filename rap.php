  <div class="column">
    <h2>¿Por que se popularizo?</h2>
    <p> En primer lugar, su capacidad para transmitir mensajes sociales y personales de una manera directa y poderosa resonó con muchas personas, especialmente en comunidades urbanas y marginadas. Las letras del rap abordan temas como la desigualdad, la injusticia social, la vida en los barrios y la lucha contra la opresión, lo que lo convirtió en una forma de expresión auténtica y relevante para muchas personas.
<br><br>
Además, el ritmo pegajoso y las habilidades líricas de los artistas de rap capturaron la atención de un amplio público, tanto dentro como fuera de las comunidades afroamericanas. A medida que el género se desarrollaba, se diversificaba y ganaba reconocimiento a nivel nacional e internacional.
<br><br>
Además, el rap se benefició del surgimiento de la industria musical hip-hop, que incluía sellos discográficos, estaciones de radio especializadas y programas de televisión dedicados al género. Esto ayudó a difundir el rap a audiencias más amplias y a establecerlo como un género musical importante.

</p>
  </div>
  <div class="column">
    <h2>Su principal audiencia </h2>
    <p>El rap inicialmente encontró su audiencia principal entre los jóvenes afroamericanos y latinos que vivían en los barrios de Nueva York, específicamente en el Bronx, donde se originó el género. Estos jóvenes, atraídos por la energía y el mensaje de las letras, así como por los ritmos innovadores, se convirtieron en los primeros seguidores del rap.
