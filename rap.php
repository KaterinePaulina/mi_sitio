<h1>El origen del rap</h1>
<div class="cont_img">
  <img src="img/rap.jpg" alt="">
</div>
<div class="cont_inf">
<p> El rap se originó en las comunidades afroamericanas y caribeñas de Nueva York, específicamente en el Bronx, durante la década de 1970. En ese momento, los DJ comenzaron a utilizar equipos de sonido para crear ritmos repetitivos y pegajosos, los cuales eran la base de las fiestas callejeras.
<br><br>
Mientras tanto, los MCs (maestros de ceremonias) comenzaron a improvisar rimas y versos sobre estos ritmos, utilizando su habilidad para el lenguaje y la rima para animar a la multitud y transmitir mensajes sociales y personales. Este intercambio entre DJ y MC sentó las bases del rap como lo conocemos hoy en día.

<br><br>
Con el tiempo, el rap se convirtió en un género musical completo con una amplia gama de estilos, desde letras políticamente cargadas hasta temas festivos y de fiesta. Además, el rap ha tenido un impacto significativo en la moda, el lenguaje y la cultura juvenil en general.
 
</p>
